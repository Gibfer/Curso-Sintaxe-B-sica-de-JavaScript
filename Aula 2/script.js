// tipos primitivos /* e */ servem para fazer blocos de comentário

// boolean
 var vOuf = false;
console.log(typeof(vOuf));

//number
 var numeroqualquer = 1;
console.log(typeof(numeroqualquer));

//string
 var nome = 'bruh'
console.log(typeof(nome))

//function
var funcao = function() {}
console.log(typeof(funcao))

